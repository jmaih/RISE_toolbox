%  SUBPARAGRAPH Adds a subparagraph to the report.
% 
%  Usage:
%    obj.subparagraph(Title)
%    obj.subparagraph(Title, 'OptionName', OptionValue, ...)
% 
%  Inputs:
%    - obj: The report object.
%    - Title: The title of the subparagraph.
%    - OptionName/OptionValue: Optional. Additional options for the subparagraph.
% 
%  Options:
%    - 'numbered': Boolean indicating whether the subparagraph should be numbered (default: true).
% 
%  Example:
%    rprt.subparagraph('Overview');
%