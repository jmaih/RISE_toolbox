%  RM constructor.
% 
%  Usage:
% 
%    obj = rm(year, month)
% 
%    obj = rm(dateString)
% 
%    obj = rm(datenumber)
% 
%  Inputs:
% 
%    - year: Year of the date.
% 
%    - month: Month of the date (1 to 12).
% 
%    - dateString: A string in the format 'mm/dd/yyyy' or 'yyyy-mm-dd'.
% 
%    - datenumber: regular date number
% 
%  Example:
% 
%    dateObj = rm(2023, 7);
% 
%    dateObj = rm('07/01/2023');
% 
%    dateObj = rm(737999);
% 
%  See also rd, rw, rq, rh, ra, ry
%